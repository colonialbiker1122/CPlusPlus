//SCAN AND PRINT CHARACTER, INTEGER AND FLOAT VALUES
#include<iostream>
using namespace std;

int main() {
    int i;
    char c;
    float f;
    cout<<"Enter a character : ";
    cin>>c;
    cout<<"Enter an integer : ";
    cin>>i;
    cout<<"Enter a floating number :";
    cin>>f;
    cout<<endl<<"Entered character is : "<<c;
    cout<<endl<<"Entered integer is : "<<i;
    cout<<endl<<"Entered floating integer is : "<<f;

    return 0;
}